import pytest
from bubble_sort import bubble_sort

def test_sort_works_with_small_list():
    input_list = [18, -11, 68, 6, 32, 53, -2]
    sorted_list = bubble_sort(input_list)
    assert sorted_list == [-11, -2, 6, 18, 32, 53, 68]

